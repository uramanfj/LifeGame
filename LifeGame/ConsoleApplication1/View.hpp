void Random(int l, int h, vector<vector<int> > &input)
	{
		int status;
		vector <int> row;
		srand(unsigned(time(0)));
		for (int i = 0; i<h; i++)
		{
			for (int j = 0; j<l; j++)
			{
				if (i == 0 || i == h - 1 || j==0||j==l-1)
					status = 0;
				else
					status = rand() % 2;
				row.push_back(status);
			}
			input.push_back(row);
			row.clear();
		}
	}

